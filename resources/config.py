from pathlib import Path

import yaml

path = Path(__file__).resolve().parents[1] / "config.yaml"


def read_yaml():
    with open(path, "r") as file:
        return yaml.safe_load(file)


def telegram():
    return read_yaml()["Telegram"]


def database():
    return read_yaml()["database"]


def console():
    return read_yaml()["console"]
